#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	srcln;
	size_t	i;

	if (!dst || !src)
		return (0);
	srcln = ft_strlen(src);
	if (size == 0)
		return (srcln);
	i = 0;
	while (src[i] && i < size - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (srcln);
}
